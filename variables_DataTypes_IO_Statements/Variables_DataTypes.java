package variables_DataTypes_IO_Statements;

public class Variables_DataTypes 
{

	public static void main(String[] args) 
	{
		
		// Declare the variables
		int a; // variable name a and data type int
		double b; // Variable name b and data type double
		char c; // variable name c and data type character
		String d; // variable name d and data type string 
		boolean e; // variable name e and data type boolean 
		
		
		// Assign the values to variables
		a=10;
		b=12.5;
		c='h';
		d="Saibersys";
		e=true;  // e=false
		
		
		// Display the result with using output statement
		System.out.println("Integer value : "+a);
		System.out.println("Floating point value : "+b);
		System.out.println("Character : "+c);
		System.out.println("String : "+d);
		System.out.println("Boolen value : "+e);
		

	}

}
